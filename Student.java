import java.sql.*;
import java.util.ArrayList;

public class Student {
    String name;
    String surname;

    public Student(String name, String surname) {
        this.name = name;
        this.surname = surname;
    }

    public String getName(String name) {
        return name;
    }

    public String getSurname(String surname) {
        return surname;
    }
}


